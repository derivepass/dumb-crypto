    fn check(key: &[u8], inputs: &[&[u8]], expected: [u8; DIGEST_SIZE]) {
        let mut hmac = HMac::new(key);

        for chunk in inputs {
            hmac.update(chunk);
        }
        assert_eq!(hmac.digest(), expected);
    }

